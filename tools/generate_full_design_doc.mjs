function lineText(chapter, lineInChapter, globalLine) {
  const chapterNumber = String(chapters.indexOf(chapter) + 1).padStart(2, '0');
  if (lineInChapter === 1) return `# ${chapterNumber}. ${chapter.title}`;
  if (lineInChapter === 2) return `- 앵커: ${chapter.anchor}`;
  if (lineInChapter === 3) return `- 핵심 명제: ${chapter.thesis}`;
  if (lineInChapter === 4) return `- 복제 금지 메모: 표면 규칙, 명칭, UI 배치, 캐릭터 문법을 그대로 가져오지 않고 감정 목적만 추출한다.`;
  if (lineInChapter === 5) return `- 검증 질문: 이 줄의 제안은 플레이어가 실제로 더 재미있게 느낄 순간을 만드는가?`;
  const idx = lineInChapter - 6;
  const lens = lenses[idx % lenses.length];
  const noun = chapter.nouns[idx % chapter.nouns.length];
  const verb = chapter.verbs[idx % chapter.verbs.length];
  const mechanic = chapter.mechanics[idx % chapter.mechanics.length];
  const outcome = chapter.outcomes[idx % chapter.outcomes.length];
  const tone = tones[(idx + chapters.indexOf(chapter)) % tones.length];
  const verb2 = verbs2[(idx * 3 + chapters.indexOf(chapter)) % verbs2.length];
  const sub = Math.floor(idx / lenses.length) + 1;
  const mode = idx % 8;
  if (mode === 0) return `- ${lens} ${sub}: '${noun}' 항목은 '${mechanic}' 시스템과 연결될 때 '${tone}' 감정을 만들고, 결과적으로 '${outcome}'를 목표로 한다.`;
  if (mode === 1) return `- ${lens} ${sub}: '${mechanic}' 시스템은 플레이어에게 '${noun}' 관련 선택으로 보여야 하며, 설명문보다 즉시 피드백이 먼저다.`;
  if (mode === 2) return `- ${lens} ${sub}: '${tone}' 감정이 약하면 '${mechanic}'의 수치 보상을 늘리기보다 타이밍, 이펙트, 파트너 반응을 먼저 조정한다.`;
  if (mode === 3) return `- ${lens} ${sub}: '${noun}' 항목을 다룰 때는 한 손 조작, 3초 이해, 7분 세션이라는 모바일 제약을 동시에 만족해야 한다.`;
  if (mode === 4) return `- ${lens} ${sub}: '${mechanic}' 시스템의 실패 상태는 억울함이 아니라 다음 판의 계획으로 남아야 하므로 복구 단서를 화면에 남긴다.`;
  if (mode === 5) return `- ${lens} ${sub}: '${outcome}'를 확인하려면 플레이테스트에서 사용 빈도, 망설인 시간, 재도전 여부를 함께 본다.`;
  if (mode === 6) return `- ${lens} ${sub}: '${noun}' 관련 수치가 과해지면 협동보다 자동전투가 강해지므로 파트너가 개입할 창을 남긴다.`;
  return `- ${lens} ${sub}: 라인 ${globalLine}의 설계 판단은 '${mechanic}' 시스템이 '${tone}' 감정으로 읽히는지, 그리고 '${outcome}'로 이어지는지를 기준으로 채택한다.`;
}

